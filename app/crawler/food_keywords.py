CATEGORY_MAP: dict[str, str] = {}
for _cat, _foods in _FOODS_BY_CATEGORY.items():
    for _f in _foods:
        FOOD_NAMES.add(_f)
        CATEGORY_MAP[_f] = _cat

# 按长度降序排列，优先匹配长食物名（"北京烤鸭" > "烤鸭"）
