# ---------------------------------------------------------------------------
# 公开函数
# ---------------------------------------------------------------------------
def match_food_in_text(text: str) -> str | None:
    """从文本中匹配具体食物名，返回最长匹配的食物名或 None。"""
    for name in _SORTED_FOOD_NAMES:
        if name in text:
            return name
    return None


def match_all_foods_in_text(text: str) -> list[str]:
    """从文本中匹配所有食物名，返回匹配列表（按长度降序）。"""
    return [name for name in _SORTED_FOOD_NAMES if name in text]


def is_food_related(text: str) -> bool:
    """判断文本是否与美食相关。"""
    if match_food_in_text(text):
        return True
    return any(kw in text for kw in FOOD_CONTEXT_KEYWORDS)


def get_category(food_name: str) -> str | None:
    """获取食物分类。"""
    return CATEGORY_MAP.get(food_name)
